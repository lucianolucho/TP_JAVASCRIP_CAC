/*8. Realice el algoritmo para calcular e informar el perímetro y el área del círculo. Se
considerará para este ejercicio que el valor de PI será 3.1416.*/

const PI = 3.1416;
var radio = 13;

var perimetro =  2.0 * PI * radio ;
var area = PI * radio * radio;  

alert("El valor de  radio  del círculo es: " + radio + " \n" +
    
    "El perímetro es: " + perimetro + " unidades \n" +
    "El área es: " + area + " unidades al cuadrado \n" );

console.log("El valor de  radio  del círculo es: " + radio  );
